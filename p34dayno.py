dayno=int(input("enter dayno:"))
if dayno==1:
    print("monday")
elif dayno==2:
    print("tuesday")
elif dayno==3:
    print("wednesday")
elif dayno==4:
    print("thursday")
elif dayno==5:
    print("friday")
elif dayno==6:
    print("saturday")
elif dayno==7:
    print("sunday")
else:
    print("invalid day number")
